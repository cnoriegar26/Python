print("Python has three numeric types: int, float, and complex")
myvalue1 = 1
print ("Tipo de dato entero (int)")
print("El valor es", myvalue1)
print(type(myvalue1))
print(str(myvalue1) + " is of the data type " + str(type(myvalue1)))
print("\n")

print ("Tipo de dato flotante (float)")
myvalue2 = 3.14
print(type(myvalue2))
print(str(myvalue2) + " is of the data type " + str(type(myvalue2)))
print("\n")

print ("Tipo de dato complex (complex)")
myvalue3 = 5j
print(type(myvalue3))
print(str(myvalue3) + " is of the data type " + str(type(myvalue3)))
print("\n")

print ("Tipo de dato Booleano (True)")
myvalue4 = True
print(type(myvalue4))
print(str(myvalue4) + " is of the data type " + str(type(myvalue4)))
print("\n")

print ("Tipo de dato Booleano (False)")
myvalue5 = False
print(type(myvalue5))
print(str(myvalue5) + " is of the data type " + str(type(myvalue5)))
